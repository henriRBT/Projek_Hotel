    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        int selectedIndexPegawai = pegawaiDropDown.getSelectedIndex();
        int selectedIndexReservasi = reservasiDropDown.getSelectedIndex();
        Pegawai selectedPegawai = listPegawai.get(selectedIndexPegawai);
        Reservasi selectedReservasi = listReservasi.get(selectedIndexReservasi);
        
        String metodePembayaran = "-";
        String status = "Check In";
        
        
        if(action.equals("Tambah")){
            Transaksi t = new Transaksi(metodePembayaran,
                                        status,  
                                        selectedPegawai,
                                        selectedReservasi);
            transaksiControl.insertDataTransaksi(t);
        }else if(action.equals("Bayar")){
            status = "Check Out";
            
            if(jRadioCash.isSelected()==true){
                metodePembayaran = "Cash";
            }else if(jRadioEWallet.isSelected()==true){
                metodePembayaran = "E-Wallet";
            }else if(jRadioCredit.isSelected()==true){
                metodePembayaran = "Kartu Credit";
            }else{
                metodePembayaran = "Kartu Debit";
            }
            
            Transaksi t = new Transaksi(selectedId, 
                                        metodePembayaran,
                                        status,  
                                        selectedPegawai,
                                        selectedReservasi);
            transaksiControl.updateDataTransaksi(t);
        }
        clearText();
        showTransaksi();
        setComponent(false);
        setEditDeleteBtn(false);
    }//GEN-LAST:event_saveBtnActionPerformed

