    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        try{
            int clickedRow = tableTransaksi.getSelectedRow();
            TableModel tableModel = tableTransaksi.getModel();
              
            if(tableModel.getValueAt(clickedRow, 5).equals("Check Out")){
                transaksiControl.deleteDataTransaksi(selectedId);
                clearText();
                showTransaksi();
                setComponent(false);
                setEditDeleteBtn(false);
                JOptionPane.showMessageDialog(null, "Data Transaksi berhasil dihapus!");
            }else{
                JOptionPane.showMessageDialog(null, "Tamu belum Check Out!");
            }
        }catch(Exception  e){
            System.out.println("Error : " + e.getMessage());
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        setEditDeleteBtn(false);
        setComponent(false);
        
        try{
            TableTransaksi transaksi = transaksiControl.showTransaksi(searchInput.getText());
            if(transaksi.getRowCount() == 0){
                clearText();
                setEditDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data Transkasi tidak ditemukan!",
                     "Konfirmasi", JOptionPane.DEFAULT_OPTION);
            }else{
                tableTransaksi.setModel(transaksi);
            }
            clearText();
        }catch(Exception e){
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    private void tableTransaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTransaksiMouseClicked
        int indexReservasi = -1;
        int indexPegawai = -1;
        setEditDeleteBtn(true);
        setComponent(false);
        bayarBtn.setEnabled(true);
        
        int clickedRow = tableTransaksi.getSelectedRow();
        TableModel tableModel = tableTransaksi.getModel();
        
        selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 0).toString());
        
        String metodePembayaran = tableModel.getValueAt(clickedRow, 3).toString();
        switch(metodePembayaran){
            case "Cash":
                jRadioCash.setSelected(true);
                jRadioEWallet.setSelected(false);
                jRadioCredit.setSelected(false);
                jRadioDebit.setSelected(false);
                break;
            case "E-Wallet":
                jRadioCash.setSelected(false);
                jRadioEWallet.setSelected(true);
                jRadioCredit.setSelected(false);
                jRadioDebit.setSelected(false);
                break;
            case "Kartu Credit":
                jRadioCash.setSelected(false);
                jRadioEWallet.setSelected(false);
                jRadioCredit.setSelected(true);
                jRadioDebit.setSelected(false);
                break;
            case "Kartu Debit":
                jRadioCash.setSelected(false);
                jRadioEWallet.setSelected(false);
                jRadioCredit.setSelected(false);
                jRadioDebit.setSelected(true);
                break;
        }
        String id_pegawai = tableModel.getValueAt(clickedRow, 1).toString();
        for(Pegawai pegawai: listPegawai){
            if(pegawai.getId().equals(id_pegawai)){
                indexPegawai = listPegawai.indexOf(pegawai);
            }
        }
        pegawaiDropDown.setSelectedIndex(indexPegawai);
        
        int id_reservasi = Integer.parseInt(tableModel.getValueAt(clickedRow, 2).toString());
        for(Reservasi reservasi: listReservasi){
            if(reservasi.getId() == id_reservasi){
                indexReservasi = listReservasi.indexOf(reservasi);
            }
        }
        reservasiDropDown.setSelectedIndex(indexReservasi);
    }//GEN-LAST:event_tableTransaksiMouseClicked

