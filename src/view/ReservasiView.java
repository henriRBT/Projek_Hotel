    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try{
            inputKosongException();
            numberFormatException();
            
            int selectedIndexPegawai = pegawaiDropDown.getSelectedIndex();
            int selectedIndexTamu = tamuDropDown.getSelectedIndex();
            Pegawai selectedPegawai = listPegawai.get(selectedIndexPegawai);
            Tamu selectedTamu = listTamu.get(selectedIndexTamu);
            
            int hargaHotel=120000;
            float subTotal, totalHarga, pajak=0.1f, hitung, totalPajak;
            
            if(Integer.parseInt(jmlTamuInput.getText())%2==0){
                hitung = Integer.parseInt(jmlTamuInput.getText())/2;
            }
            else{
                hitung = (Integer.parseInt(jmlTamuInput.getText())/2)+1;
            }
            
            subTotal = (hitung * hargaHotel)*Integer.parseInt(durasiInput.getText());
            totalPajak = subTotal * pajak;
            totalHarga = subTotal + totalPajak;
            
            if(action.equals("Tambah")){
                Reservasi r = new Reservasi(tglCheckInput.getText(),
                                            Integer.parseInt(durasiInput.getText()), 
                                            Integer.parseInt(jmlTamuInput.getText()),
                                            totalHarga,
                                            tglResInput.getText(), 
                                            selectedPegawai,
                                            selectedTamu);
                reservasiControl.insertDataReservasi(r);
            }else if(action.equals("Ubah")){
                Reservasi r = new Reservasi(selectedId, 
                                            tglCheckInput.getText(),
                                            Integer.parseInt(durasiInput.getText()), 
                                            Integer.parseInt(jmlTamuInput.getText()),
                                            totalHarga,
                                            tglResInput.getText(), 
                                            selectedPegawai,
                                            selectedTamu);
                reservasiControl.updateDataReservasi(r);
            }
            clearText();
            showReservasi();
            setComponent(false);
            setEditDeleteBtn(false);
        }catch(InputKosongException e){
            JOptionPane.showMessageDialog(this, e.message());
        }catch(NumberFormatException e2){
            JOptionPane.showMessageDialog(this, e2.message());
        }
    }//GEN-LAST:event_saveBtnActionPerformed

