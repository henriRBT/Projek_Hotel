    private void tableReservasiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableReservasiMouseClicked
        int indexPegawai = -1;
        int indexTamu = -1;
        setEditDeleteBtn(true);
        setComponent(false);
        
        int clickedRow = tableReservasi.getSelectedRow();
        TableModel tableModel = tableReservasi.getModel();
        
        selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 0).toString());
        tglCheckInput.setText(tableModel.getValueAt(clickedRow, 3).toString());
        durasiInput.setText(tableModel.getValueAt(clickedRow, 4).toString());
        jmlTamuInput.setText(tableModel.getValueAt(clickedRow, 5).toString());
        tglResInput.setText(tableModel.getValueAt(clickedRow, 7).toString());

        String id_pegawai = tableModel.getValueAt(clickedRow, 1).toString();
        for(Pegawai pegawai: listPegawai){
            if(pegawai.getId().equals(id_pegawai)){
                indexPegawai = listPegawai.indexOf(pegawai);
            }
        }
        pegawaiDropDown.setSelectedIndex(indexPegawai);
        
        int id_tamu = Integer.parseInt(tableModel.getValueAt(clickedRow, 2).toString());
        for(Tamu tamu: listTamu){
            if(tamu.getId() == id_tamu){
                indexTamu = listTamu.indexOf(tamu);
            }
        }
        tamuDropDown.setSelectedIndex(indexTamu);
    }//GEN-LAST:event_tableReservasiMouseClicked

