    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int getAnswer = JOptionPane.showConfirmDialog(rootPane,
            "Apakah yakin ingin menghapus data Reservasi? ", "Konfirmasi",
            JOptionPane.YES_NO_OPTION);
        
        if(getAnswer == JOptionPane.YES_OPTION){
            try{
                reservasiControl.deleteDataReservasi(selectedId);
                clearText();
                showReservasi();
                setComponent(false);
                setEditDeleteBtn(false);
                JOptionPane.showMessageDialog(null, "Data Reservasi berhasil dihapus!");
            }catch(Exception  e){
                System.out.println("Error : " + e.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Data Reservasi tidak jadi dihapus!");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

