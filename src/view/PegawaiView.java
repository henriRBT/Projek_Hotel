    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try{
            inputKosongException();
            idInvalidException();
            
            String jenisKelamin = "";
            
            if(jRadioPria.isSelected()==true){
                jenisKelamin = "Pria";
            }else if(jRadioWanita.isSelected()==true){
                jenisKelamin = "Wanita";
            }else{
                pilihanKosongException();
            }
            
            
            Pegawai p = new Pegawai(idInput.getText(), 
                                    namaInput.getText(), 
                                    roleInput.getText(), 
                                    jenisKelamin,
                                    noTelpInput.getText());

            if(action.equals("Tambah")){
                pegawaiControl.insertDataPegawai(p);
                JOptionPane.showMessageDialog(this, "Data Pegawai berhasil ditambah!");
            }else if(action.equals("Ubah")){
                pegawaiControl.updateDataPegawai(p, idInput.getText());
                JOptionPane.showMessageDialog(this, "Data Pegawai berhasil diupdate!");
            }
            jRadioPria.setEnabled(false);
            jRadioWanita.setEnabled(false);
            clearText();
            showPegawai();
            setComponent(false);
            setEditDeleteBtn(false);
        }catch (InputKosongException e){
            JOptionPane.showMessageDialog(this, e.message());
        }catch (IDInvalidException e2){
            JOptionPane.showMessageDialog(this, e2.message());
        }catch (PilihanKosongException e3){
            JOptionPane.showMessageDialog(this, e3.message());
        }catch (Exception e){}
    }//GEN-LAST:event_saveBtnActionPerformed

