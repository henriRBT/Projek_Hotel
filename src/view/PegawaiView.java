    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int getAnswer = JOptionPane.showConfirmDialog(rootPane,
            "Apakah yakin ingin menghapus data Pegawai? ", "Konfirmasi",
            JOptionPane.YES_NO_OPTION);
        
        if(getAnswer == JOptionPane.YES_OPTION){
            try{
                pegawaiControl.deleteDataPegawai(idInput.getText());
                clearText();
                sexBtnGroup.clearSelection();
                showPegawai();
                JOptionPane.showMessageDialog(null, "Data Pegawai berhasil dihapus!");
            }catch(Exception  e){
                System.out.println("Error : " + e.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Data Pegawai tidak jadi dihapus!");
        }
        setComponent(true);
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        setEditDeleteBtn(true);
        setComponent(false);
        
        try{
            Pegawai pegawai = pegawaiControl.searchPegawai(searchInput.getText());
            if(pegawai == null){
                clearText();
                sexBtnGroup.clearSelection();
                setEditDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data tidak ditemukan!",
                     "Konfirmasi", JOptionPane.DEFAULT_OPTION);
            }else{
                idInput.setText(pegawai.getId());
                namaInput.setText(pegawai.getNama());
                roleInput.setText(pegawai.getRole());
                noTelpInput.setText(pegawai.getNo_telp());
                
                if(pegawai.getJenis_kelamin().equalsIgnoreCase("Pria")){
                    jRadioPria.setSelected(true);
                }else{
                    jRadioWanita.setSelected(true);
                }
            }
        }catch(Exception e){
            System.out.println("Error : " + e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

