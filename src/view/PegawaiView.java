    public void idInvalidException() throws IDInvalidException{
        if(idInput.getText().length()<5 || idInput.getText().length()>7){
            throw new IDInvalidException();
        }
    }
    
    public void pilihanKosongException() throws PilihanKosongException{
        if(!jRadioPria.isSelected() || !jRadioWanita.isSelected()){
            throw new PilihanKosongException();
        }
    }
    
    public void inputKosongException() throws InputKosongException{
        if(idInput.getText().isEmpty() || namaInput.getText().isEmpty()
                    || roleInput.getText().isEmpty()
                    || noTelpInput.getText().isEmpty()) {
            throw new InputKosongException();
        }
    }

